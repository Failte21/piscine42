void	ft_putchar(char c);

void	ft_print_comb(void)
{
	int nb[3];

	nb[0] = 48;
	while (nb[0] <= '7')
	{
		nb[1] = nb[0] + 1;
		while (nb[1] <= '8')
		{
			nb[2] = nb[1] + 1;
			while (nb[2] <= '9')
			{
				ft_putchar(nb[0]);
				ft_putchar(nb[1]);
				ft_putchar(nb[2]);
				if (nb[0] != '7' || nb[1] != '8' || nb[2] != '9')
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
				nb[2]++;
			}
			nb[1]++;
		}
		nb[0]++;
	}
}
